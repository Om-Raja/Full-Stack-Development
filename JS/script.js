// how to loop an array 
// using forEach Method
var arr = [1, 2, 3, 4];
arr.forEach(function(value){
    console.log(10 + value);
});

arr.forEach(function(hello){
    console.log((20 + hello)) ;
});

var friends = ["Om", "Nikita", "Kashish", "Shikha", "Devendra"];
friends.forEach(function(names){
    console.log(names);
});